$(".menuButton").click(function() {
    $(".homeScreen").hide();
    $(".chatScreen").hide();
    $(".menuScreen").show();
});

$(".message").click(function() {
    $(".menuScreen").hide();
    $(".chatScreen").show();
}); 

$(".music").click(function() {
    $(".menuScreen").hide();
    $(".homeScreen").hide();
    $(".musicPlayer").show();
});

