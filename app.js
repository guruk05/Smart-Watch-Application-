$(".menuButton").click(function() {
    $(".homeScreen").hide();
    $(".chatScreen").hide();
    $(".menuScreen").show();
});

$(".message").click(function() {
    $(".menuScreen").hide();
    $(".musicPlayer").hide();
    $(".chatDetails").hide();
    $(".chatScreen").show();
}); 

$(".music").click(function() {
    $(".menuScreen").hide();
    $(".chats").hide();
    $(".chatTime").hide();
    $(".messages").hide();
    $(".messages1").hide();
    $(".messages2").hide();
    $(".chatDetails").hide();
    $(".musicPlayer").show();
}); 

